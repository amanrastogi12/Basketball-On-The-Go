package com.example.namankhanna.basketballscorecard;

public class Player {
    String name;
    int tNum;

    public Player(String name, int tNum) {
        this.name = name;
        this.tNum = tNum;
    }

    public String getName() {
        return name;
    }

    public int gettNum() {
        return tNum;
    }
}
